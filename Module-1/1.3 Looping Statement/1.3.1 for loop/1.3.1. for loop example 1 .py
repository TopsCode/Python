"""
range() 

in python we have range function 

which accept 3 arguments 

range([start],stop,[step])

start : starting of loop 

stop : ending point of loop 

step : increment or decrement of loop sequence

in python range function start and step are optional 
-----------------------------------------------
by default it will start from 0 and increment by 1 

"""
